package fundamentals;

public class PrimitiveTypes {

    public static void main(String[] args) {

        // Whole Numerals
        byte years = 23;
        short flightNumbers = 523;
        int id = 454545;
        long number = 923723723273L;

        // Real Numerals
        float breaks = 82373.4F;
        double huge = 83748374834.9349834;

        // Boolean
        boolean yes = true;

        //Character
        char status = 'A';

    }
}
